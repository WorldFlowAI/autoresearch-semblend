#!/usr/bin/env python3
"""FM2 Semantic Staleness Benchmark (ProphetKV Gap Validation).

MOTIVATION
----------
Failure Mode 2 (FM2) from Hudson's primer: Wagner-Fischer edit distance measures
string edit distance, not attention-weighted relevance. High lexical overlap ≠
semantic equivalence when key entities are swapped.

This benchmark creates "entity-swap" test pairs: donor and query share identical
structure but differ in one or two named entities (person, place, organization).
These have HIGH cosine similarity (same topic/structure) and HIGH lexical overlap,
but the semantically CRITICAL tokens differ.

Expected SemBlend behavior on entity-swap:
  - High hit rate (cosine sim ≥ 0.60 due to structural overlap)
  - If FM2 is a problem: PPL ratio degrades when entity position falls within
    a matched LMCache chunk (donor's KV "remembers" the wrong entity)
  - If FM2 is handled: PPL ratio ≈ 1.0 even with entity swaps

METHODOLOGY
-----------
For each cluster:
1. COLD: Run cold inference on entity-swap variation
2. SEED: Register original article as donor
3. SEMBLEND: Run entity-swap variation through SemBlend
4. Measure: hit_rate, PPL ratio, ROUGE-L

Entity swaps are generated by replacing named entities at key positions.
We use three swap intensities:
  - light:  1 entity swapped (e.g., person name)
  - medium: 2 entities swapped (person + organization)
  - heavy:  3 entities swapped (person + org + location)

If FM2 is real, we expect PPL to degrade most for "heavy" swaps.

USAGE
-----
    python -m benchmarks.e2e.entity_swap_bench \\
        --endpoint http://localhost:8100 \\
        --model Qwen/Qwen2.5-7B-Instruct-AWQ \\
        --clusters-file benchmarks/data/cnn_dailymail_clusters.json \\
        --target-length 8192 \\
        --n-clusters 16 \\
        --max-tokens 64 \\
        --output results/entity-swap/qwen_8k.json
"""
