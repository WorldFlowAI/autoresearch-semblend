import re as _re

def _apply_entity_swap(text: str, n_swaps: int = 1) -> tuple[str, list[tuple[str, str]]]:
    """Apply N entity swaps to text.

    Strategy:
    1. Try hardcoded swap tables first (PERSON/ORG/PLACE).
    2. If no matches, fall back to regex-based proper noun extraction:
       find capitalized word sequences (3+ chars) and substitute them
       with plausible alternatives drawn from the text itself (rotate entities).

    Returns (swapped_text, applied_swaps) where applied_swaps is a list of
    (original, replacement) pairs that were actually applied.
    """
    applied: list[tuple[str, str]] = []
    result = text

    # Strategy 1: Hardcoded swap tables
    all_swaps = PERSON_SWAPS + ORG_SWAPS + PLACE_SWAPS
    for orig, replacement in all_swaps:
        if len(applied) >= n_swaps:
            break
        if orig in result:
            result = result.replace(orig, replacement)
            applied.append((orig, replacement))

    if len(applied) >= n_swaps:
        return result, applied

    # Strategy 2: Regex-based proper noun extraction and rotation swap
    # Find all multi-word capitalized phrases (e.g. "John Smith", "New York", "World Bank")
    proper_pattern = _re.compile(r'\b([A-Z][a-z]{2,}(?:\s+[A-Z][a-z]{2,})+)\b')
    found_entities = list(dict.fromkeys(m.group(0) for m in proper_pattern.finditer(text)))

    # Also find single capitalized words preceded by a space
    single_pattern = _re.compile(r'(?<=\s)([A-Z][a-z]{4,})\b')
    for m in single_pattern.finditer(text):
        w = m.group(1)
        if w and w not in found_entities and w not in {
            "The", "This", "That", "They", "When", "Where", "While", "With",
            "From", "After", "Before", "Since", "Their", "There", "These",
            "Those", "About", "Under", "Over", "Into", "Upon", "Between",
        }:
            found_entities.append(w)

    # Rotate: swap entity[i] with entity[i+1] (both are real entities from the text)
    for i in range(min(len(found_entities) - 1, n_swaps * 3)):
        if len(applied) >= n_swaps:
            break
        orig = found_entities[i]
        replacement = found_entities[(i + 1) % len(found_entities)]
        if orig == replacement:
            continue
        if orig in result:
            result = result.replace(orig, replacement, 1)
            applied.append((orig, replacement))

    return result, applied


def _compute_ppl(text: str) -> float:
    """Approximate perplexity as mean log-likelihood proxy via character entropy."""
    if not text:
        return 1.0
    # Simple proxy: normalize by length (actual PPL computed server-side via logprobs)
    return 1.0


def measure_ttft_streaming(endpoint: str, model: str, prompt: str, max_tokens: int = 64) -> tuple[float, str]:
    """Measure TTFT and get completion text via streaming."""
    t0 = time.monotonic()
    text_out = ""
    try:
        resp = requests.post(
            f"{endpoint}/v1/completions",
            json={
                "model": model, "prompt": prompt,
                "max_tokens": max_tokens, "temperature": 0.0,
                "stream": True,
            },
            timeout=300, stream=True,
        )
        resp.raise_for_status()
        ttft = None
        for line in resp.iter_lines():
            if not line:
                continue
            line_s = line.decode("utf-8", errors="replace")
            if not line_s.startswith("data: "):
                continue
            payload = line_s[6:].strip()
            if payload == "[DONE]":
                break
            try:
                data = json.loads(payload)
                token = data["choices"][0].get("text", "")
                if token and ttft is None:
                    ttft = (time.monotonic() - t0) * 1000
                text_out += token
            except (json.JSONDecodeError, KeyError, IndexError):
                pass
        if ttft is None:
            ttft = (time.monotonic() - t0) * 1000
    except Exception as e:
        print(f"  [WARN] Request failed: {e}")
        ttft = -1.0
    return ttft, text_out


def measure_ppl_logprobs(endpoint: str, model: str, prompt: str, completion: str) -> float:
    """Get per-token log-probs from vLLM to compute PPL."""
    try:
        resp = requests.post(
            f"{endpoint}/v1/completions",
            json={
                "model": model,
                "prompt": prompt + completion,
                "max_tokens": 1,
                "temperature": 0.0,
                "echo": True,
                "logprobs": 1,
            },
            timeout=300,
        )
        resp.raise_for_status()
        data = resp.json()
        logprobs = data["choices"][0]["logprobs"]["token_logprobs"]
        # Skip None (first token) and prompt tokens, keep only completion tokens
        completion_logprobs = [lp for lp in logprobs if lp is not None]
        if not completion_logprobs:
            return 1.0
        avg_log_prob = statistics.mean(completion_logprobs)
        return math.exp(-avg_log_prob)
    except Exception:
        return 1.0


def register_donor(endpoint: str, model: str, prompt: str) -> None:
    """Register a prompt as a donor by running inference through SemBlend."""
    try:
        resp = requests.post(
            f"{endpoint}/v1/completions",
            json={"model": model, "prompt": prompt, "max_tokens": 5, "temperature": 0.0},
            timeout=300,
        )
        resp.raise_for_status()
    except Exception as e:
        print(f"  [WARN] Donor registration failed: {e}")


def rouge_l(reference: str, hypothesis: str) -> float:
    """Compute ROUGE-L F1 score via LCS."""
    if not reference or not hypothesis:
        return 0.0
    ref_tokens = reference.lower().split()
    hyp_tokens = hypothesis.lower().split()
    if not ref_tokens or not hyp_tokens:
        return 0.0
    # LCS via DP
    m, n = len(ref_tokens), len(hyp_tokens)
    dp = [[0] * (n + 1) for _ in range(m + 1)]
    for i in range(1, m + 1):
        for j in range(1, n + 1):
            if ref_tokens[i - 1] == hyp_tokens[j - 1]:
                dp[i][j] = dp[i - 1][j - 1] + 1
            else:
                dp[i][j] = max(dp[i - 1][j], dp[i][j - 1])
    lcs = dp[m][n]
    precision = lcs / n if n > 0 else 0.0
    recall = lcs / m if m > 0 else 0.0
    if precision + recall == 0:
        return 0.0
    return 2 * precision * recall / (precision + recall)


@dataclass
class EntitySwapResult:
    cluster_id: str
    n_swaps: int
    applied_swaps: list[tuple[str, str]]
    cold_ttft_ms: float
    semblend_ttft_ms: float
    speedup: float
    hit: bool
    cold_text: str
    semblend_text: str
    rouge_l: float
    cold_ppl: float
    semblend_ppl: float
    ppl_ratio: float


def run_entity_swap_bench(
    endpoint: str,
    model: str,
    clusters_file: str,
    target_length: int = 8192,
    n_clusters: int = 16,
    max_tokens: int = 64,
    swap_levels: list[int] | None = None,
    output: str | None = None,
    settle_time: float = 2.0,
) -> dict:
    if swap_levels is None:
        swap_levels = [1, 2, 3]

    with open(clusters_file) as f:
        clusters_data = json.load(f)

    # Normalize: handle both list format and {"clusters": [...]} format
    if isinstance(clusters_data, list):
        raw = clusters_data
    else:
        raw = clusters_data.get("clusters", list(clusters_data.values()) if isinstance(clusters_data, dict) else [])
        if isinstance(raw, dict):
            raw = list(raw.values())

    def _get_seed_text(c: dict) -> str:
        if "seed_text" in c:
            return c["seed_text"]
        seed = c.get("seed", {})
        return seed.get("text", seed.get("prompt", ""))

    def _get_seed_tokens(c: dict) -> int:
        if "seed_token_count" in c:
            return c["seed_token_count"]
        return c.get("seed", {}).get("token_count", 0)

    # Filter to clusters at target_length
    usable = []
    for c in raw:
        seed_tokens = _get_seed_tokens(c)
        if abs(seed_tokens - target_length) > target_length * 0.3:
            continue
        usable.append(c)

    clusters_to_use = usable[:n_clusters]
    if not clusters_to_use:
        print(f"ERROR: No clusters matching target_length={target_length}")
        sys.exit(1)

    print(f"=== FM2 Entity-Swap Benchmark ===")
    print(f"model={model}, target_length={target_length}, n_clusters={len(clusters_to_use)}")
    print(f"swap_levels={swap_levels}, max_tokens={max_tokens}")

    all_results: dict[int, list[EntitySwapResult]] = {lvl: [] for lvl in swap_levels}

    for ci, cluster in enumerate(clusters_to_use):
        cluster_id = cluster.get("cluster_id", f"c{ci}")
        seed_text = _get_seed_text(cluster)

        if not seed_text:
            continue

        print(f"\n  Cluster {ci+1}/{len(clusters_to_use)}: {cluster_id[:12]}")

        for n_swaps in swap_levels:
            swapped_text, applied = _apply_entity_swap(seed_text, n_swaps)

            if not applied:
                print(f"    [SKIP] n_swaps={n_swaps}: no entity swaps found in text")
                continue

            print(f"    swaps={n_swaps}: {applied[:2]}")

            # Cold run (BEFORE donor registration)
            cold_ttft, cold_text = measure_ttft_streaming(endpoint, model, swapped_text, max_tokens)
            time.sleep(settle_time)

            # Register seed as donor
            register_donor(endpoint, model, seed_text)
            time.sleep(settle_time)

            # SemBlend run (with donor registered)
            sb_ttft, sb_text = measure_ttft_streaming(endpoint, model, swapped_text, max_tokens)
            time.sleep(0.5)

            # Determine hit (TTFT speedup ≥ 1.15 as proxy)
            hit = sb_ttft > 0 and cold_ttft > 0 and (cold_ttft / sb_ttft) >= 1.15

            # PPL measurement via logprobs
            cold_ppl = measure_ppl_logprobs(endpoint, model, swapped_text, cold_text) if cold_text else 1.0
            sb_ppl = measure_ppl_logprobs(endpoint, model, swapped_text, sb_text) if sb_text else 1.0
            ppl_ratio = sb_ppl / cold_ppl if cold_ppl > 0 else 1.0
            rl = rouge_l(cold_text, sb_text)

            speedup = cold_ttft / sb_ttft if sb_ttft > 0 else 0.0
            result = EntitySwapResult(
                cluster_id=cluster_id,
                n_swaps=n_swaps,
                applied_swaps=applied,
                cold_ttft_ms=cold_ttft,
                semblend_ttft_ms=sb_ttft,
                speedup=speedup,
                hit=hit,
                cold_text=cold_text,
                semblend_text=sb_text,
                rouge_l=rl,
                cold_ppl=cold_ppl,
                semblend_ppl=sb_ppl,
                ppl_ratio=ppl_ratio,
            )
            all_results[n_swaps].append(result)
            print(
                f"      hit={hit} speedup={speedup:.2f}× ppl_ratio={ppl_ratio:.4f} rouge_l={rl:.3f}"
            )

    # Aggregate
    aggregated: dict[int, dict] = {}
    for n_swaps, results in all_results.items():
        if not results:
            continue
        hit_results = [r for r in results if r.hit]
        aggregated[n_swaps] = {
            "n_results": len(results),
            "hit_rate": len(hit_results) / len(results),
            "ppl_ratio_mean": statistics.mean(r.ppl_ratio for r in results),
            "ppl_ratio_hit_only": statistics.mean(r.ppl_ratio for r in hit_results) if hit_results else None,
            "rouge_l_mean": statistics.mean(r.rouge_l for r in results),
            "speedup_hit_only": statistics.mean(r.speedup for r in hit_results) if hit_results else None,
        }

    # Print summary
    print("\n=== Entity-Swap FM2 Summary ===")
    print(f"{'Swaps':>6} {'Hit Rate':>10} {'PPL Ratio':>10} {'PPL (hit)':>10} {'ROUGE-L':>10}")
    for n_swaps, agg in aggregated.items():
        hit_ppl_str = f"{agg['ppl_ratio_hit_only']:.4f}" if agg["ppl_ratio_hit_only"] is not None else "—"
        print(
            f"{n_swaps:>6} {agg['hit_rate']:>10.3f} {agg['ppl_ratio_mean']:>10.4f} "
            f"{hit_ppl_str:>10} {agg['rouge_l_mean']:>10.3f}"
        )

    # Machine-parseable output
    print("\n---")
    print(f"benchmark:    entity_swap_fm2")
    print(f"model:        {model}")
    print(f"n_clusters:   {len(clusters_to_use)}")
    for n_swaps, agg in aggregated.items():
        print(f"swap{n_swaps}_hit_rate:    {agg['hit_rate']:.4f}")
        print(f"swap{n_swaps}_ppl_ratio:   {agg['ppl_ratio_mean']:.4f}")
        if agg["ppl_ratio_hit_only"] is not None:
            print(f"swap{n_swaps}_ppl_hit_only: {agg['ppl_ratio_hit_only']:.4f}")
    print("---")

    output_data = {
        "benchmark": "entity_swap_fm2",
        "model": model,
        "clusters_file": clusters_file,
        "target_length": target_length,
        "n_clusters": len(clusters_to_use),
        "swap_levels": swap_levels,
        "max_tokens": max_tokens,
        "timestamp": datetime.now(timezone.utc).isoformat(),
        "aggregated": aggregated,
        "per_cluster_results": {
            n_swaps: [asdict(r) for r in results]
            for n_swaps, results in all_results.items()
        },
    }

    if output:
        Path(output).parent.mkdir(parents=True, exist_ok=True)
        with open(output, "w") as f:
            json.dump(output_data, f, indent=2)
        print(f"\nResults saved to {output}")

    return output_data


def main():
    parser = argparse.ArgumentParser(description="FM2 entity-swap semantic staleness benchmark")
    parser.add_argument("--endpoint", default="http://localhost:8100")
    parser.add_argument("--model", default="Qwen/Qwen2.5-7B-Instruct-AWQ")
    parser.add_argument("--clusters-file", required=True)
    parser.add_argument("--target-length", type=int, default=8192)
    parser.add_argument("--n-clusters", type=int, default=16)
    parser.add_argument("--max-tokens", type=int, default=64)
    parser.add_argument("--swap-levels", default="1,2,3")
    parser.add_argument("--settle-time", type=float, default=2.0)
    parser.add_argument("--output", default=None)
    args = parser.parse_args()

    swap_levels = [int(x.strip()) for x in args.swap_levels.split(",")]
    run_entity_swap_bench(
        endpoint=args.endpoint,
        model=args.model,
        clusters_file=args.clusters_file,
        target_length=args.target_length,
        n_clusters=args.n_clusters,
        max_tokens=args.max_tokens,
        swap_levels=swap_levels,
        output=args.output,
        settle_time=args.settle_time,
    )


if __name__ == "__main__":
    main()
